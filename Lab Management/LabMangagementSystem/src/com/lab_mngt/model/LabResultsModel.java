package com.lab_mngt.model;

public class LabResultsModel {
	private int labId;
	private String labName;
	private int capacity;
	private String type;
	private String location;
	private int noOfSystems;
	private String projector;
	private String vcFacility;
	private int noOfWhiteBoards;
	private String status;
	
	public int getLabId() {
		return labId;
	}
	public void setLabId(int labId) {
		this.labId = labId;
	}
	public String getLabName() {
		return labName;
	}
	public void setLabName(String labName) {
		this.labName = labName;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public int getNoOfSystems() {
		return noOfSystems;
	}
	public void setNoOfSystems(int noOfSystems) {
		this.noOfSystems = noOfSystems;
	}
	public String getProjector() {
		return projector;
	}
	public void setProjector(String projector) {
		this.projector = projector;
	}
	public String getVcFacility() {
		return vcFacility;
	}
	public void setVcFacility(String vcFacility) {
		this.vcFacility = vcFacility;
	}
	public int getNoOfWhiteBoards() {
		return noOfWhiteBoards;
	}
	public void setNoOfWhiteBoards(int noOfWhiteBoards) {
		this.noOfWhiteBoards = noOfWhiteBoards;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
}
